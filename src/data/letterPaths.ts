// ============================================================
// Letter Path Data for Letter Tracing
// Simplified checkpoint-based paths for lowercase letters a-z
// All coordinates normalized 0-1 (multiply by canvas size)
// ============================================================

export interface LetterStroke {
  /** Ordered checkpoints the user should pass through */
  points: [number, number][];
}

export interface LetterPath {
  /** Each stroke is drawn separately (e.g., 'i' has a vertical line + dot) */
  strokes: LetterStroke[];
  /** Where the first stroke begins */
  startPoint: [number, number];
  /** All guide dots flattened from strokes for simple hit-testing */
  guideDots: [number, number][];
}

function buildGuideDots(strokes: LetterStroke[]): [number, number][] {
  const dots: [number, number][] = [];
  for (const stroke of strokes) {
    for (const pt of stroke.points) {
      dots.push(pt);
    }
  }
  return dots;
}

function lp(strokes: LetterStroke[]): LetterPath {
  const guideDots = buildGuideDots(strokes);
  return {
    strokes,
    startPoint: strokes[0]?.points[0] ?? [0.5, 0.5],
    guideDots,
  };
}


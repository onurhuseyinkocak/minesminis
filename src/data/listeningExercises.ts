/**
 * Pick exercises for Phase 4.
 * Tries to match theme first; if not enough, fills from same level, then any level.
 */
export function pickListeningExercises(
  userLevel: 1 | 2 | 3 | 4 | 5,
  theme: string,
  count = 3,
): ListeningExercise[] {
  // 1. Exact match: same level + theme
  const exact = ALL_LISTENING_EXERCISES.filter(
    (e) => e.level === userLevel && (e.theme === theme || e.theme === 'General'),
  );

  // 2. Same level, any theme
  const sameLevel = ALL_LISTENING_EXERCISES.filter((e) => e.level === userLevel);

  // 3. Adjacent level fallback (level ±1)
  const adjacent = ALL_LISTENING_EXERCISES.filter(
    (e) => Math.abs(e.level - userLevel) <= 1,
  );

  // Pool priority: exact > sameLevel > adjacent > all
  const pool = exact.length >= count ? exact : sameLevel.length >= count ? sameLevel : adjacent.length >= count ? adjacent : ALL_LISTENING_EXERCISES;

  // Shuffle and pick
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
